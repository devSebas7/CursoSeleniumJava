package javaBasico;

public class Ejemplo_Loop_While {

	public static void main(String[] args) {
		int a = 1;
		
		// Salir del While cuando se mayor a 10
		while (a <= 10) {
			System.out.println("El valor de A es: " + a);
			
			// Incrementamos el valor de a en cada iteraciÃ³n
			a++;
		}

	}

}
